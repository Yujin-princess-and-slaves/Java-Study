package output;

public class Ex02 {
    public static void main(String[] args) {
        // \n : new line
        System.out.println("Hello\nWorld!");

        // \t : tab
        System.out.println("Hello \t\t World!");

        // ' '
        System.out.println("이지우씨는 '35분' 지각하셨습니다.");

        // \"
        System.out.println("배재현씨는 집이 \"가장\" 가깝습니다");

        // \\
        System.out.println("나의 로컬 주소는 \\\\127.0.0.1 입니다.");
    }
}
